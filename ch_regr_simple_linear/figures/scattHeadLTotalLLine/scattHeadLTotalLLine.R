require(openintro)
data(COL)
data(possum)
set.seed(1)

myPDF('scattHeadLTotalLLine.pdf', 6, 4,
      mar = c(3.4, 3.4, 0.5, 1),
      mgp = c(2.3, 0.5, 0))
these <- c(48, 42, 3)
plot(possum$total_l[-these], possum$head_l[-these],
     pch = 20,
     col = COL[1, 2],
     cex = 1.7,
     frame.plot = FALSE,
     axes=FALSE,
     xlab = 'Total Length (cm)',
     ylab = 'Head Length (mm)')
axis(1,seq(70,100,5))
axis(2,seq(80,110,5))
rect(par("usr")[1],par("usr")[3],par("usr")[2],par("usr")[4],col = COL[5,9], border=NA)

points(possum$total_l[these] + rnorm(3,0,0.02),
       possum$head_l[these] + rnorm(3,0,0.02),
       pch = c(3, 4, 2),
       col = COL[4],
       cex = 1.5,
       lwd = 2.5)
abline(41, 0.59, col = COL[5])
for(i in 1:3){
  y2 <- 41 + 0.59 * possum$total_l[these[i]]
  lines(rep(possum$total_l[these[i]], 2),
        c(possum$head_l[these[i]], y2),
        lty = 3,
        lwd = 1.5,
        col = COL[4])
}
dev.off()
